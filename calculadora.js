let number1 = Number(prompt("Dígite o primeiro número"))
let number2 = Number(prompt("Dígite o segundo número"))

let soma = number1 + number2
let subtração = number1 - number2
let multiplicação = number1 * number2
let divisão = number1 / number2
let resto = number1 % number2

alert(`A soma dos dois números é: ${soma}`)
alert(`A subtração dos dois números é: ${subtração}`)
alert(`A multiplicação dos dois números é: ${multiplicação}`)
alert(`A divisão dos dois números é: ${divisão}`)
alert(`O resto da divisão dos dois números é: ${resto}`)


